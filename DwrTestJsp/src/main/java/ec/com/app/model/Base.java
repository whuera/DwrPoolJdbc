package ec.com.app.model;


/**
 * The Class Base.
 */
public abstract class Base {
	
	/** The code transaction. */
	private String codeTransaction;
	
	/** The desc transaction. */
	private String descTransaction;
	
	/** The code emp. */
	private String codeEmp;
	
	/** The code user. */
	private String codeUser;
	
	/**
	 * Gets the code transaction.
	 *
	 * @return the code transaction
	 */
	public String getCodeTransaction() {
		return codeTransaction;
	}
	
	/**
	 * Sets the code transaction.
	 *
	 * @param codeTransaction the new code transaction
	 */
	public void setCodeTransaction(String codeTransaction) {
		this.codeTransaction = codeTransaction;
	}
	
	/**
	 * Gets the desc transaction.
	 *
	 * @return the desc transaction
	 */
	public String getDescTransaction() {
		return descTransaction;
	}
	
	/**
	 * Sets the desc transaction.
	 *
	 * @param descTransaction the new desc transaction
	 */
	public void setDescTransaction(String descTransaction) {
		this.descTransaction = descTransaction;
	}
	
	/**
	 * Gets the code emp.
	 *
	 * @return the code emp
	 */
	public String getCodeEmp() {
		return codeEmp;
	}
	
	/**
	 * Sets the code emp.
	 *
	 * @param codeEmp the new code emp
	 */
	public void setCodeEmp(String codeEmp) {
		this.codeEmp = codeEmp;
	}
	
	/**
	 * Gets the code user.
	 *
	 * @return the code user
	 */
	public String getCodeUser() {
		return codeUser;
	}
	
	/**
	 * Sets the code user.
	 *
	 * @param codeUser the new code user
	 */
	public void setCodeUser(String codeUser) {
		this.codeUser = codeUser;
	}
	

}
